"""

Escreva um programa que calcule a soma dos números de 1 a 100 e imprima o resultado.

"""

#
#       NÃO ENTENDI O ENUNCIADO!
#


soma = 0
for numero in range(1, 101):
    soma += numero
    print(soma)

print("A soma dos números de 1 a 100 é:", soma)


# Explicação:

#     Iniciamos a variável soma com o valor 0, que será usada para acumular a soma dos números.
#     O for percorre a sequência de números de 1 a 100 (novamente, o segundo parâmetro do range não está incluso).
#     Em cada iteração do loop, o valor atual de numero é adicionado à variável soma.
#     Após o término do loop, a soma total é impressa na tela.

# Ao executar esse código, você verá a saída que mostra a soma dos números de 1 a 100.
    


