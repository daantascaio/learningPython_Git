"""
For + Range
range -> range(start, stop, step)
"""


numbers1 = range(10)


for numero in numbers1:
    print(numero)

print('#####################EXEMPLO 1#####################')
######################################

numbers2 = range(0, 10)


for numero in numbers2:
    print(numero)
print('#####################EXEMPLO 2#####################')
######################################

numbers3 = range(0, 10, 2)


for numero in numbers3:
    print(numero)

print('#####################EXEMPLO 3#####################')
######################################

numbers4 = range(0, -10, -1)


for numero in numbers4:
    print(numero)

print('#####################EXEMPLO 4#####################')
######################################

