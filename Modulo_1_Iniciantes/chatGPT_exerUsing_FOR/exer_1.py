"""

Escreva um programa que imprima todos os números de 1 a 10.

"""
# eu
for numbers in range(0, 11):
    print(numbers)


# ChatGPT
for numero in range(1, 11):
    print(numero)

# O for é uma estrutura de loop que nos permite percorrer uma sequência de elementos. No caso, usamos range(1, 11), que gera uma sequência de números de 1 a 10 (o segundo parâmetro do range não está incluso).
# A variável numero assume cada valor dentro da sequência gerada pelo range.
# A cada iteração do loop, o valor de numero é impresso na tela.
# Ao executar esse código, você verá a saída que imprime os números de 1 a 10, um em cada linha.
