"""

Escreva um programa que peça ao usuário para digitar uma palavra e, em seguida, imprima cada letra da palavra em uma linha separada.

"""


# Claro! Vamos resolver o Exercício 8, que consiste em escrever um programa que solicite ao usuário para digitar
# uma palavra e, em seguida, imprima cada letra da palavra em uma linha separada.


palavra = input("Digite uma palavra: ")

for letra in palavra:
    print(letra)

#     Explicação:

#     O programa solicita ao usuário que digite uma palavra usando a função input, e o valor digitado é armazenado na variável palavra.
#     Em seguida, utilizamos um loop for para iterar por cada letra da palavra.
#     A cada iteração do loop, a variável letra assume um valor correspondente a uma das letras da palavra.
#     Usamos o comando print dentro do loop para imprimir cada letra em uma linha separada.

# Dessa forma, o programa irá imprimir cada letra da palavra digitada pelo usuário em linhas separadas.

